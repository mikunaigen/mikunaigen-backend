CREATE TABLE IF NOT EXISTS alimentos_dataset (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo_minsa VARCHAR(20),
    nombre VARCHAR(150) NOT NULL,
    categoria VARCHAR(50) NOT NULL,
    energia_kcal NUMERIC(10, 4) NOT NULL,
    agua_g NUMERIC(10, 4),
    proteinas_g NUMERIC(10, 4),
    grasa_total_g NUMERIC(10, 4),
    carbohidratos_totales_g NUMERIC(10, 4),
    carbohidratos_disponibles_g NUMERIC(10, 4),
    fibra_g NUMERIC(10, 4),
    cenizas_g NUMERIC(10, 4),
    calcio_mg NUMERIC(10, 4),
    fosforo_mg NUMERIC(10, 4),
    zinc_mg NUMERIC(10, 4),
    hierro_mg NUMERIC(10, 4),
    beta_caroteno_ug NUMERIC(10, 4),
    vitamina_a_ug NUMERIC(10, 4),
    tiamina_mg NUMERIC(10, 4),
    riboflavina_mg NUMERIC(10, 4),
    niacina_mg NUMERIC(10, 4),
    vitamina_c_mg NUMERIC(10, 4),
    acido_folico_ug NUMERIC(10, 4),
    sodio_mg NUMERIC(10, 4),
    potasio_mg NUMERIC(10, 4),
    costo_kg_soles NUMERIC(10, 2) NOT NULL,
    meses_disponibilidad INTEGER ARRAY NOT NULL,
    modificado_por UUID,
    fecha_modificacion TIMESTAMP
);
